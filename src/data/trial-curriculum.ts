/** WEAK_UNIT 名 → subtopic 配列。フォームのフィルタリングで使う。 */
export function subtopicsFor(unit: string): TrialSubtopic[] {
  const found = TRIAL_CURRICULUM.find((u) => u.unit === unit);
  return found?.subtopics ?? [];
}

/** すべての subtopic value のフラット集合。API バリデーションで使う。 */
export const ALL_SUBTOPIC_VALUES: ReadonlySet<string> = new Set(
  TRIAL_CURRICULUM.flatMap((u) => u.subtopics.map((s) => s.value)),
);
